/*  
Bitwise operators. 
*/

#include <stdio.h>
#include <stddef.h>

int main() {

    int a=32,b=34;
    printf("a&b is equal to %d\n",a&b );
    printf("a|b is equal to %d\n", a|b);
    printf("a^b is equal to %d \n", a^b);
    printf("~a is equal to %d\n", ~a);//gives us 2's compliment

    //check right and left bitwise shifts 



    return 0;
}
